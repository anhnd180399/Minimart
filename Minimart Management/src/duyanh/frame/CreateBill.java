    private void btnAddToBagActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToBagActionPerformed
        // TODO add your handling code here:
        String id, name, categoryID;
        int amount, price, total;
        id = txtProID.getText();
        name = txtProName.getText();
        categoryID = txtCategoryID.getText();
        if (id.equals("")) {
            JOptionPane.showMessageDialog(this, "Please choose an product!");
        } else {
            try {
                amount = Integer.parseInt(txtAmount.getText());
                if (amount <= 0) {
                    JOptionPane.showMessageDialog(this, "Amount must be greater than 0");
                } else {
                    price = Integer.parseInt(txtUnitPrice.getText());
                    total = amount * price;
                    totalAll += total;
                    Vector v = new Vector();
                    v.add(id);
                    v.add(name);
                    v.add(categoryID);
                    v.add(amount);
                    v.add(price);
                    v.add(total);
                    tblBillModel.addRow(v);
                    txtTotalAll.setText(String.valueOf(totalAll));
                    btnNew();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Wrong input type!");
            }
        }

    }//GEN-LAST:event_btnAddToBagActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblBill.getSelectedRow();
        if (row >= 0) {
            int totalRow = (Integer) tblBillModel.getValueAt(row, 5);
            totalAll -= totalRow;
            tblBillModel.removeRow(row);
            txtTotalAll.setText(String.valueOf(totalAll));
            JOptionPane.showMessageDialog(this, "Remove success!");
        } else {
            JOptionPane.showMessageDialog(this, "Please choose an product!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

