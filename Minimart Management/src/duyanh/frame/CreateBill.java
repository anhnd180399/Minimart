    private void btnAddCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCustomerActionPerformed
        // TODO add your handling code here:
        String phone, name, address, valid = "";
        phone = txtCusPhone.getText();
        name = txtCusName.getText();
        address = txtCusAddress.getText();

        if (phone.equals("")) {
            valid += "Phone can't be blank! \n";
        }
        if (name.equals("")) {
            valid += "Name can't be blank! \n";
        }
        if (address.equals("")) {
            valid += "Address can't be blank! \n";
        }
        if (valid.equals("")) {
            CustomerDAO dao = new CustomerDAO();
            CustomerDTO dto = new CustomerDTO(phone, name, address);
            try {
                if (dao.insertCustomer(dto)) {
                    JOptionPane.showMessageDialog(this, "Insert success!");
                    hide();
                } else {
                    JOptionPane.showMessageDialog(this, "Insert failed!");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        } else {
            JOptionPane.showMessageDialog(this, valid);
        }
    }//GEN-LAST:event_btnAddCustomerActionPerformed

