    private void txtProIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtProIDActionPerformed
        // TODO add your handling code here:
        String id = txtProID.getText();
        if (id.equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter an product!");
        } else {
            try {
                ProductDAO dao = new ProductDAO();
                ProductDTO dto = dao.getProductByID(id);
                if (dto == null) {
                    JOptionPane.showMessageDialog(this, "This product does not exist!");
                    txtProID.setText("");
                } else {
                    txtProName.setText(dto.getProName());
                    txtCategoryID.setText(dto.getCategoryID());
                    txtUnitPrice.setText(String.valueOf(dto.getPrice()));
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_txtProIDActionPerformed

