    public List<BillDTO> collectDataOfBill() {
        List<BillDTO> billDetail = new ArrayList<>();
        try {
            String orderID;
            Date date;
            SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            date = df.parse(txtDate.getText());
            java.sql.Date orderDate = new java.sql.Date(date.getTime());
            orderID = txtBillID.getText();
            for (int i = 0; i < tblBillModel.getRowCount(); i++) {
                String proID, proName, categoryID;
                int price, amount;
                proID = (String) tblBillModel.getValueAt(i, 0);
                proName = (String) tblBillModel.getValueAt(i, 1);
                categoryID = (String) tblBillModel.getValueAt(i, 2);
                amount = (Integer) tblBillModel.getValueAt(i, 3);
                price = (Integer) tblBillModel.getValueAt(i, 4);
                billDetail.add(new BillDTO(orderID, proID, proName, categoryID, price, amount, orderDate));
            }
        } catch (ParseException ex) {
            Logger.getLogger(CreateBill.class.getName()).log(Level.SEVERE, null, ex);
        }
        return billDetail;
    }

